CREATE OR REPLACE FORCE VIEW MCRE_OWN.V_MCRE0_APP_PM_ELENCO_PIANI
(
   COD_ABI_ISTITUTO,
   COD_ABI_CARTOLARIZZATO,
   COD_NDG,
   COD_SNDG,
   DESC_ISTITUTO,
   DESC_NOME_CONTROPARTE,
   COD_COMPARTO,
   COD_FILIALE,
   COD_PERCORSO,
   COD_GRUPPO_ECONOMICO,
   DESC_GRUPPO_ECONOMICO,
   COD_STRUTTURA_COMPETENTE_RG,
   DESC_STRUTTURA_COMPETENTE_RG,
   COD_STRUTTURA_COMPETENTE_AR,
   DESC_STRUTTURA_COMPETENTE_AR,
   COD_STRUTTURA_COMPETENTE_FI,
   DESC_STRUTTURA_COMPETENTE_FI,
   COD_STRUTTURA_COMPETENTE,
   DTA_DECORRENZA_STATO,
   DTA_SCADENZA_STATO,
   COD_PROCESSO,
   ID_WORKFLOW,
   VAL_NUMERO_PIANO,
   DTA_PIANO,
   DTA_SCADENZA,
   DTA_SCADENZA_PRECEDENTE,
   DTA_VALIDAZIONE,
   VAL_NOTE_OPERATIVE,
   FLG_PRESENZA_PIANO,
   VAL_GG_PM,
   FLG_PIANO_MODIFICATO,
   FLG_PRESA_VISIONE_MODIFICA
)
AS
   SELECT a.COD_ABI_ISTITUTO,
          a.COD_ABI_CARTOLARIZZATO,
          a.COD_NDG,
          a.COD_SNDG,
          a.DESC_ISTITUTO,
          a.DESC_NOME_CONTROPARTE,
          a.COD_COMPARTO,
          A.COD_FILIALE,
          a.cod_percorso,
          a.COD_GRUPPO_ECONOMICO,
          a.DESC_GRUPPO_ECONOMICO,
          a.COD_STRUTTURA_COMPETENTE_RG,
          a.DESC_STRUTTURA_COMPETENTE_RG,
          a.COD_STRUTTURA_COMPETENTE_AR,
          a.DESC_STRUTTURA_COMPETENTE_AR,
          a.COD_STRUTTURA_COMPETENTE_FI,
          a.DESC_STRUTTURA_COMPETENTE_FI,
          a.COD_STRUTTURA_COMPETENTE,
          a.DTA_DECORRENZA_STATO,
          a.DTA_SCADENZA_STATO,
          a.COD_PROCESSO,
          NVL (P.ID_WORKFLOW, 0) ID_WORKFLOW,
          p.ID_PIANO VAL_NUMERO_PIANO,
          p.DTA_PIANO,
          P.DTA_SCADENZA,
          P.DTA_SCADENZA_PRECEDENTE,
          P.DTA_PIANO_VALIDATO DTA_VALIDAZIONE,
          P.VAL_NOTE_OPERATIVE,
          DECODE (P.ID_PIANO, NULL, 'N', 'S') flg_presenza_piano,
          TRUNC (SYSDATE) - a.dta_decorrenza_stato VAL_GG_PM,
          NVL (P.FLG_PIANO_MODIFICATO, '0') FLG_PIANO_MODIFICATO,
          NVL (P.FLG_PRESA_VISIONE_MODIFICA, '0') FLG_PRESA_VISIONE_MODIFICA
     FROM V_MCRE0_APP_UPD_FIELDS a, T_MCRE0_APP_GEST_PM p
    WHERE     a.COD_ABI_CARTOLARIZZATO = p.COD_ABI_CARTOLARIZZATO(+)
          AND a.COD_NDG = p.COD_NDG(+)
          AND A.COD_PERCORSO = P.COD_PERCORSO(+)
          AND A.COD_STATO = 'PM'
          AND P.FLG_PIANO_ANNULLATO(+) = 'N'
          AND NVL (P.DTA_SCADENZA, SYSDATE + 1) >= SYSDATE;
