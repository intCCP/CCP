CREATE OR REPLACE FORCE VIEW MCRE_OWN.V_MCRES_APP_REPORT_RAPPORTI
(
   ID_DPER,
   COD_ABI,
   COD_NDG,
   COD_RAPPORTO,
   COD_SSA,
   DTA_APERTURA_RAPP,
   DTA_CHIUSURA_RAPP,
   COD_DIVISA,
   VAL_IMP_GBV,
   VAL_IMP_NBV,
   DTA_NBV,
   VAL_IMP_VANTATO,
   VAL_IMP_GBV_INIZIALE,
   VAL_IMP_NBV_INIZIALE,
   VAL_IMP_TOT_INCASSI,
   FLG_RAPP_FONDO_TERZO,
   FLG_RAPP_CARTOLARIZZATO,
   FLG_RAPP_ESTERO,
   COD_TIPO_FONDO_TERZO,
   VAL_SOCIETA_CARTOLARIZZAZIONE,
   COD_ABI_CARTOLARIZZANTE,
   COD_FORMA_TECNICA,
   DESC_FORMA_TECNICA,
   COD_UO_RAPPORTO,
   COD_SNDG,
   DTA_INS,
   DTA_UPD,
   COD_OPERATORE_INS_UPD,
   FLG_AGEVOLATO,
   FLG_CONTRIBUTO,
   FLG_CONFIDI,
   VAL_PERC_RISCHIO_IST
)
AS
   SELECT "ID_DPER",
          "COD_ABI",
          "COD_NDG",
          "COD_RAPPORTO",
          "COD_SSA",
          "DTA_APERTURA_RAPP",
          "DTA_CHIUSURA_RAPP",
          "COD_DIVISA",
          "VAL_IMP_GBV",
          "VAL_IMP_NBV",
          "DTA_NBV",
          "VAL_IMP_VANTATO",
          "VAL_IMP_GBV_INIZIALE",
          "VAL_IMP_NBV_INIZIALE",
          "VAL_IMP_TOT_INCASSI",
          "FLG_RAPP_FONDO_TERZO",
          "FLG_RAPP_CARTOLARIZZATO",
          "FLG_RAPP_ESTERO",
          "COD_TIPO_FONDO_TERZO",
          "VAL_SOCIETA_CARTOLARIZZAZIONE",
          "COD_ABI_CARTOLARIZZANTE",
          "COD_FORMA_TECNICA",
          "DESC_FORMA_TECNICA",
          "COD_UO_RAPPORTO",
          "COD_SNDG",
          "DTA_INS",
          "DTA_UPD",
          "COD_OPERATORE_INS_UPD",
          "FLG_AGEVOLATO",
          "FLG_CONTRIBUTO",
          "FLG_CONFIDI",
          "VAL_PERC_RISCHIO_IST"
     FROM T_MCRES_APP_RAPPORTI P
    WHERE     DTA_CHIUSURA_RAPP = TO_DATE ('99991231', 'YYYYMMDD')
          AND ID_DPER <
                 (SELECT TO_CHAR (ID_DPER - 5, 'YYYYMMDD')
                    FROM T_MCRES_WRK_LAST_ACQUISIZIONE A
                   WHERE COD_FLUSSO = 'RAPPORTI' AND A.COD_ABI = P.COD_ABI);
