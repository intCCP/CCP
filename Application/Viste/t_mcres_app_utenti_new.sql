          VAL_COGNOME COGNOME,
          VAL_NOME NOME,
          '1' FLG_GESTORE_ABILITATO,
          COD_PUNTO_OPERATIVO COD_PRESIDIO,
          VAL_SOCIETA,
          VAL_SOCIETA_UOG,
          COD_MACRO,
          DESC_MACRO,
          COD_CLONE,
          VAL_INDIRIZZO,
          VAL_COMUNE,
          VAL_PROV,
          VAL_CAP,
          VAL_TELEFONO,
          VAL_FAX,
          VAL_CELLULARE,
          VAL_EMAIL,
          COD_FIGURA_PROFESSIONALE,
          DESC_FIGURA_PROFESSIONALE
     FROM t_mcres_app_gestori;
