(
   ID_UTENTE,
   COD_MATRICOLA,
   COD_FISCALE,
   COGNOME,
   NOME,
   FLG_GESTORE_ABILITATO,
   COD_PRESIDIO,
   VAL_SOCIETA,
   VAL_SOCIETA_UOG,
   COD_MACRO,
   DESC_MACRO,
   COD_CLONE,
   VAL_INDIRIZZO,
   VAL_COMUNE,
   VAL_PROV,
   VAL_CAP,
