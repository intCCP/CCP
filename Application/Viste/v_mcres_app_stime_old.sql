(
   COD_ABI,
   COD_NDG,
   COD_RAPPORTO,
   FLG_TIPO_DATO,
   COD_PROTOCOLLO_DELIBERA,
   COD_SNDG,
   DTA_STIMA,
   DESC_CAUSA_PREV_RECUPERO,
   FLG_RECUPERO_TOT,
   COD_UO_STIMA,
   VAL_IMP_PREV_PREGR,
